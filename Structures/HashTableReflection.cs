namespace Structures;

public abstract class HashTableReflection
{
    // Моё описание хэш-таблицы в целом совпадает с эталонным по смыслу: есть методы Put, Delete,
    // Contains, GetSize, которые соответствуют put, remove, get, size в эталоне. Также в обоих
    // вариантах можно узнать статус последней вставки и удаления через GetPutStatus и GetDeleteStatus.
    // Отличия в том, что у меня есть дополнительные методы Clear и GetCapacity, которых нет в эталоне.
    // Кроме того, у меня явно перечислены возможные значения статусов (PutOk, PutErr и т.д.), а в 
    // эталоне они описаны только словами. Ещё один момент — в эталоне отдельно сказано, что вставка 
    // может не произойти из-за коллизий, а у меня это пока не прописано. В целом за исключением
    // некоторых деталей, моё решение довольно близко к эталонному.
}